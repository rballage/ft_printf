#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	char	d;
	int		i;
	char	*occ;

	d = c;
	i = 0;
	occ = NULL;
	while (s[i])
	{
		if (s[i] == d)
		{
			occ = (char *)(&(s[i]));
		}
		i++;
	}
	if (s[i] == '\0' && d == '\0')
		occ = (char *)(&(s[i]));
	return (occ);
}
