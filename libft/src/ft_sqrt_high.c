int		ft_sqrt_high(int nb)
{
	int i;

	if (nb <= 0)
	{
		return (0);
	}
	if (nb >= 1000000000)
	{
		i = 31622;
	}
	else
		i = 1;
	while (i * i < nb)
		i++;
	return (i);
}
