int	ft_longlen(long nb)
{
	int i;

	i = 0;
	if (nb < 0)
	{
		if (nb == -2147483648)
			nb += 1;
		nb *= -1;
		i++;
	}
	while (nb > 9)
	{
		i++;
		nb /= 10;
	}
	i++;
	return (i);
}
