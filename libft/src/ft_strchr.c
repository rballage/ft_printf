#include <string.h>

char	*ft_strchr(const char *s, int c)
{
	char	d;
	int		i;

	d = c;
	i = 0;
	while (s[i])
	{
		if (s[i] == d)
			return (char *)((&(s[i])));
		i++;
	}
	if (s[i] == '\0' && d == '\0')
		return (char *)((&(s[i])));
	return (NULL);
}
