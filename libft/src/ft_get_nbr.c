int	ft_get_nbr(char *str)
{
	int i;
	int nbr;
	int neg;

	i = 0;
	nbr = 0;
	neg = 1;
	if (str[0] == '-')
	{
		neg = -1;
		str++;
	}
	while (str[i])
	{
		nbr = (nbr * 10) + (str[i] - '0');
		i++;
	}
	return (nbr * neg);
}
